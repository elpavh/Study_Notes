// Ciclo for y su uso en JavaScript
// El ciclo for se utiliza para repetir un bloque de código un número específico de veces.

// La sintaxis básica del ciclo for es:
// for (variable; condición; incremento) {
//     // Código a ejecutar en cada iteración
// }

let list = ["eat", "sleep", "code", "study", "repeat"];

for (let i = 0; i < list.length; i++) {
    console.log(list[i]); // Imprime cada elemento de la lista
}